export function calculatePercentile(sex, weight, length) {
	let child = newWeightChart[sex][Math.floor(length) + 0.5];
	let { L, M, S } = child;
	let xDivM = weight / M;
	let zScore = (Math.pow(xDivM, L) - 1) / (L * S);
	let index;
	if (zScore < 0) {
		index = Math.floor(zScore * -1 * 100);
		return (1 - ss.standardNormalTable[index]) * 100;
	}
	index = Math.floor(zScore * 100);
	return ss.standardNormalTable[index] * 100;
}

